<h3>Como crear una cotización</h3>
<br><p>Para crear una cotización debe de hacer click en la opcion Pre-Orden del menu lateral de la derecha para que se despliegue la opción Cotización:</p>
<br>
<div class="row-fluid text-center">
	<br><img src="<?php echo Yii::app()->theme->baseUrl; ?>/img/coth1.png" alt="Ayuda requerimiento.">
</div>
<br><p>Una vez ingresado al módulo Cotización usted verá un cuadro resumen donde se mostrarán los requerimientos que se ha hecho y coloreados según el estado actual de dicho requerimiento:</p>
<div class="row-fluid text-center">
	<br><img src="<?php echo Yii::app()->theme->baseUrl; ?>/img/coth2.png" alt="Ayuda requerimiento.">
</div>
<br><p>Usted tambien podra buscar un requerimiento especifico suyo por su número:</p>
<div class="row-fluid text-center">
	<br><img src="<?php echo Yii::app()->theme->baseUrl; ?>/img/coth3.png" alt="Ayuda requerimiento.">
</div>
<br><p>Ahora para añadir una cotización haga click en el botón + que esta en la columna Opc del requerimiento a cotizar:</p>
<div class="row-fluid text-center">
	<br><img src="<?php echo Yii::app()->theme->baseUrl; ?>/img/coth4.png" alt="Ayuda requerimiento.">
</div>
<br><p>Busque un proveedor y coloque el valor total de la cotización y haga click en el botón +:</p>
<div class="row-fluid text-center">
	<br><img src="<?php echo Yii::app()->theme->baseUrl; ?>/img/coth5.png" alt="Ayuda requerimiento.">
</div>
<br><p>Cuando añada una cotización, el sistema automaticamente asignara la Buena-Pro:</p>
<div class="row-fluid text-center">
	<br><img src="<?php echo Yii::app()->theme->baseUrl; ?>/img/coth6.png" alt="Ayuda requerimiento.">
</div>
<br><p>Una vez que haya añadido las cotizaciones, hace click en el botón Analizar y se desplegara el formulario para detallar la cotización a la que se le asigno la Buena-Pro:</p>
<div class="row-fluid text-center">
	<br><img src="<?php echo Yii::app()->theme->baseUrl; ?>/img/coth7.png" alt="Ayuda requerimiento.">
</div>
<br><p>Una vez que haya detallado la cotización haga click en el botón Imprimir Acta de Otorgamiento y Generar Orden de Compra:</p>
<div class="row-fluid text-center">
	<br><img src="<?php echo Yii::app()->theme->baseUrl; ?>/img/coth8.png" alt="Ayuda requerimiento.">
</div>