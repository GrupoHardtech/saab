<h3>Como crear un requerimiento</h3>
<br><p>Para crear un requerimiento debe de hacer click en la opcion Pre-Orden del menu lateral de la derecha para que se despliegue la opción Requerimiento:</p>
<br>
<div class="row-fluid text-center">
	<br><img src="<?php echo Yii::app()->theme->baseUrl; ?>/img/reqh1.png" alt="Ayuda requerimiento.">
	<br><br>
	<br><img src="<?php echo Yii::app()->theme->baseUrl; ?>/img/reqh2.png" alt="Ayuda requerimiento.">
</div>
<br><p>Una vez ingresado al módulo Requerimiento usted verá un cuadro resumen donde se mostrarán los requerimientos que uste ha hecho y coloreados según el estado actual de dicho requerimiento:</p>
<div class="row-fluid text-center">
	<br><img src="<?php echo Yii::app()->theme->baseUrl; ?>/img/reqh3.png" alt="Ayuda requerimiento.">
</div>
<br><p>Usted tambien podra buscar un requerimiento especifico suyo por su número:</p>
<div class="row-fluid text-center">
	<br><img src="<?php echo Yii::app()->theme->baseUrl; ?>/img/reqh4.png" alt="Ayuda requerimiento.">
</div>
<br><p>Ahora para crear un requrimiento haga click en el botón derecho Crear requerimiento:</p>
<div class="row-fluid text-center">
	<br><img src="<?php echo Yii::app()->theme->baseUrl; ?>/img/reqh5.png" alt="Ayuda requerimiento.">
</div>
<br><p>Seleccione en que Clasificador se encuentran los bienes:</p>
<div class="row-fluid text-center">
	<br><img src="<?php echo Yii::app()->theme->baseUrl; ?>/img/reqh6.png" alt="Ayuda requerimiento.">
</div>
<br><p>Busque un bien y digite la cantidad a requerir, luego haga click en el boton +:</p>
<div class="row-fluid text-center">
	<br><img src="<?php echo Yii::app()->theme->baseUrl; ?>/img/reqh7.png" alt="Ayuda requerimiento.">
</div>
<br><p>Una vez que haya añadido los bienes, seleccione la meta administrativa:</p>
<div class="row-fluid text-center">
	<br><img src="<?php echo Yii::app()->theme->baseUrl; ?>/img/reqh8.png" alt="Ayuda requerimiento.">
</div>
<br><p>Una vez que este seguro de que haya ingresado bien los bienes, haga click en Guardar, de lo contrario haga click en Cancelar:</p>
<div class="row-fluid text-center">
	<br><img src="<?php echo Yii::app()->theme->baseUrl; ?>/img/reqh9.png" alt="Ayuda requerimiento.">
</div>
